// Get font bitmap for a character
// Returns pointer to 8-byte bitmap, or NULL if character not supported
const uint8_t* font_get_bitmap(uint16_t unicode) {
    // ASCII range
    if (unicode >= 32 && unicode <= 126) {
        return extended_font[unicode - 32];
    }
    
    // Cyrillic uppercase А-Я (Unicode 0x0410-0x042F)
    if (unicode >= 0x0410 && unicode <= 0x042F) {
        return extended_font[128 + (unicode - 0x0410)];
    }
    
    // Cyrillic lowercase а-п (Unicode 0x0430-0x043F)
    if (unicode >= 0x0430 && unicode <= 0x043F) {
        return extended_font[160 + (unicode - 0x0430)];
    }
    
    // Cyrillic lowercase р-я (Unicode 0x0440-0x044F)
    if (unicode >= 0x0440 && unicode <= 0x044F) {
        return extended_font[176 + (unicode - 0x0440)];
    }
    
    // Ё and ё
    if (unicode == 0x0401) return extended_font[134]; // Ё
    if (unicode == 0x0451) return extended_font[166]; // ё
    
    return 0; // Character not found
}

// Convert UTF-8 byte sequence to Unicode codepoint
// Returns the codepoint, and advances *str pointer
uint16_t utf8_to_unicode(const char** str) {
    const unsigned char* s = (const unsigned char*)*str;
    uint8_t c = *s++;
    
    if ((c & 0x80) == 0) {
        // Single byte (ASCII)
        *str = (const char*)s;
        return c;
    } else if ((c & 0xE0) == 0xC0) {
        // Two bytes
        uint16_t cp = ((c & 0x1F) << 6) | (*s & 0x3F);
        *str = (const char*)(s + 1);
        return cp;
    } else if ((c & 0xF0) == 0xE0) {
        // Three bytes
        uint16_t cp = ((c & 0x0F) << 12) | ((s[0] & 0x3F) << 6) | (s[1] & 0x3F);
        *str = (const char*)(s + 2);
        return cp;
    }
    
    // Invalid UTF-8
    *str = (const char*)s;
    return 0;
}

