#include <stdint.h>
#include <io.h> //

// Внешние функции для печати
extern void terminal_putchar(char c);

// Простая таблица скан-кодов (US QWERTY)
unsigned char kbdus[128] = {
    0,  27, '1', '2', '3', '4', '5', '6', '7', '8', '9', '0', '-', '=', '\b',
    '\t', 'q', 'w', 'e', 'r', 't', 'y', 'u', 'i', 'o', 'p', '[', ']', '\n',
    0, 'a', 's', 'd', 'f', 'g', 'h', 'j', 'k', 'l', ';', '\'', '`', 0,
    '\\', 'z', 'x', 'c', 'v', 'b', 'n', 'm', ',', '.', '/', 0, '*', 0, ' ', 0
};

void isr_handler(uint32_t int_no) {
    // Номер прерывания для клавиатуры — 33 (IRQ1 + смещение PIC 32)
    if (int_no == 33) {
        // Читаем скан-код из порта данных клавиатуры
        uint8_t scancode = inb(0x60); 

        // Проверяем, что это нажатие (а не отпускание клавиши — бит 7 не установлен)
        if (!(scancode & 0x80)) {
            char c = kbdus[scancode];
            if (c != 0) {
                terminal_putchar(c);
            }
        }
    }

    // ВАЖНО: Отправка сигнала EOI (End of Interrupt) в контроллеры PIC
    // Если прерывание пришло от ведомого контроллера (IRQ 8-15)
    if (int_no >= 40) {
        outb(0xA0, 0x20); 
    }
    // Сигнал мастер-контроллеру
    outb(0x20, 0x20); 
}